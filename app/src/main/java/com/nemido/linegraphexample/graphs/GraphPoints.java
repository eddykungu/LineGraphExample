package com.nemido.linegraphexample.graphs;

public class GraphPoints {
    private float x;
    private float y;

    public GraphPoints(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
